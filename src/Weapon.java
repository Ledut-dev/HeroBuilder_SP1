public class Weapon{

        String name;
        int damage;
        int durability;
        int weight;
        double value;

        public Weapon(String name, int damage, int durability, int weight, double value){

            this.name = name;
            this.damage = damage;
            this.durability = durability;
            this.weight = weight;
            this.value = value;
        }


}
